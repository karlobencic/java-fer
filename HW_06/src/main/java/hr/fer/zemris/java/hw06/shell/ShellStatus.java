package hr.fer.zemris.java.hw06.shell;

/**
 * The {@code ShellStatus} enum defines an action that should be executed after
 * a command.
 * 
 * @author Karlo Bencic
 * 
 */
public enum ShellStatus {

	/** Continues with execution of the shell. */
	CONTINUE,

	/** Terminates the shell. */
	TERMINATE
}
