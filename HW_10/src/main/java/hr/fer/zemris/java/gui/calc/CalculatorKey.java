package hr.fer.zemris.java.gui.calc;

/**
 * The {@code CalculatorKey} enum defines calculator key types.
 * 
 * @author Karlo Bencic
 * 
 */
public enum CalculatorKey {

	/** The binary operator key. */
	BINARY,

	/** The unary operator key. */
	UNARY,

	/** The dot sign operator key. */
	DOT_SIGN,

	/** The push pop operator key. */
	PUSH_POP,

	/** The number key. */
	NUMBER,

	/** The clr reset key. */
	CLR_RESET
}
