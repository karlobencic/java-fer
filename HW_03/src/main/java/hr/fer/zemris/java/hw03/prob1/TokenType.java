package hr.fer.zemris.java.hw03.prob1;

/**
 * The enum TokenType represent the type of the token used by the lexer. Types
 * can be EOF, WORD, NUMBER and SYMBOL.
 * 
 * @author Karlo Bencic
 * 
 */
public enum TokenType {

	/** The eof. */
	EOF,
	/** The word. */
	WORD,
	/** The number. */
	NUMBER,
	/** The symbol. */
	SYMBOL
}
