package hr.fer.zemris.java.custom.scripting.exec;

/**
 * The enum ValueOperation represents a simple operation such as add, substract,
 * multiply and divide.
 * 
 * @author Karlo Bencic
 * 
 */
public enum ValueOperation {

	/** The add operation. */
	ADD,

	/** The substract operation. */
	SUBSTRACT,

	/** The multiply operation. */
	MULTIPLY,

	/** The divide operation. */
	DIVIDE
}
